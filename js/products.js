let currentPage = 1;
const itemsPerPage = 9;
let currentCategory = "All Products";
let searchQuery = "";

// Render products
function renderProducts() {
  const productsGrid = document.getElementById("productsContainer");
  if (!productsGrid) return;

  let filteredProducts = productsData;

  // Filter by category
  if (currentCategory !== "All Products") {
    filteredProducts = productsData.filter(
      (product) => product.category === currentCategory
    );
  }

  // Filter by search
  if (searchQuery) {
    filteredProducts = filteredProducts.filter(
      (product) =>
        product.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
        product.description.toLowerCase().includes(searchQuery.toLowerCase())
    );
  }

  // Calculate pagination
  const totalPages = Math.ceil(filteredProducts.length / itemsPerPage);
  const startIndex = (currentPage - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;
  const currentProducts = filteredProducts.slice(startIndex, endIndex);

  // Render products
  productsGrid.innerHTML = currentProducts
    .map(
      (product, index) => `
        <div class="col-lg-4 col-md-6 mb-4">
            <div class="card h-100 product-card">
                <img src="${product.image}" class="card-img-top" alt="${product.name}" 
                     onerror="this.src='images/products/deluxe-wooden-case-universal-gun-cleaning-kit-with-gun-rest-544209.jpg'">
                <div class="card-body">
                    <span class="badge bg-primary mb-2">${product.category}</span>
                    <h5 class="card-title">${product.name}</h5>
                    <p class="card-text text-muted">${product.description}</p>
                </div>
                <div class="card-footer bg-white border-top-0">
                    <a href="inquiry.html" class="btn btn-primary w-100">Inquiry Now</a>
                </div>
            </div>
        </div>
    `
    )
    .join("");

  // Update pagination
  renderPagination(totalPages);
}

// Render pagination
function renderPagination(totalPages) {
  const pagination = document.getElementById("paginationContainer");
  if (!pagination) return;
  
  if (totalPages === 0) {
    pagination.innerHTML = "";
    return;
  }

  if (totalPages <= 1) {
    pagination.innerHTML = "";
    return;
  }

  let paginationHTML = '<ul class="pagination justify-content-center">';

  // Previous button
  paginationHTML += `
        <li class="page-item ${currentPage === 1 ? "disabled" : ""}">
            <a class="page-link" href="#" onclick="changePage(${
              currentPage - 1
            }); return false;">Previous</a>
        </li>
    `;

  // Page numbers
  for (let i = 1; i <= totalPages; i++) {
    if (
      i === 1 ||
      i === totalPages ||
      (i >= currentPage - 1 && i <= currentPage + 1)
    ) {
      paginationHTML += `
                <li class="page-item ${i === currentPage ? "active" : ""}">
                    <a class="page-link" href="#" onclick="changePage(${i}); return false;">${i}</a>
                </li>
            `;
    } else if (i === currentPage - 2 || i === currentPage + 2) {
      paginationHTML += `
                <li class="page-item disabled">
                    <span class="page-link">...</span>
                </li>
            `;
    }
  }

  // Next button
  paginationHTML += `
        <li class="page-item ${currentPage === totalPages ? "disabled" : ""}">
            <a class="page-link" href="#" onclick="changePage(${
              currentPage + 1
            }); return false;">Next</a>
        </li>
    `;
  
  paginationHTML += '</ul>';
  pagination.innerHTML = paginationHTML;
}

// Render categories
function renderCategories() {
  const categoriesContainer = document.getElementById("categoriesContainer");
  if (!categoriesContainer) return;
  
  categoriesContainer.innerHTML = categories
    .map(
      (category) => `
        <a href="#" class="list-group-item list-group-item-action category-item ${
          category === currentCategory ? "active" : ""
        }" onclick="filterByCategory('${category}'); return false;">
          ${category}
        </a>
      `
    )
    .join("");
}

// Change page
function changePage(page) {
  const totalPages = Math.ceil(
    (currentCategory === "All Products"
      ? productsData
      : productsData.filter((p) => p.category === currentCategory)
    ).length / itemsPerPage
  );

  if (page < 1 || page > totalPages) return;

  currentPage = page;
  renderProducts();
  window.scrollTo({ top: 0, behavior: "smooth" });
}

// Filter by category
function filterByCategory(category) {
  currentCategory = category;
  currentPage = 1;
  renderProducts();

  // Update active state
  document.querySelectorAll(".category-item").forEach((item) => {
    if (item.textContent.trim() === category) {
      item.classList.add("active");
    } else {
      item.classList.remove("active");
    }
  });
}

// Search products
function searchProducts() {
  const searchInput = document.getElementById("searchInput");
  if (searchInput) {
    searchQuery = searchInput.value;
    currentPage = 1;
    renderProducts();
  }
}

// Initialize
document.addEventListener("DOMContentLoaded", function () {
  renderCategories();
  renderProducts();

  // Add search event listener
  const searchInput = document.getElementById("searchInput");
  if (searchInput) {
    searchInput.addEventListener("input", searchProducts);
  }
});
